#přetypování na int(číslo)
něco = int(input("Zadejte číslo: "))
#první podmínka if a použítí or(nebo)
if něco < 5 or něco ==5:
    print("menší jak nebo rovno 5")
#druhá podmínka elif
elif něco > 6:
    print("Větší jak 6")
#třetí podmínka else
else:
    print("rovno šest")

